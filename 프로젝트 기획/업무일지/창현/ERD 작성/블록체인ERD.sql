CREATE TABLE `MEMBER` (
	`MEMBER_SEQ`	INT	NOT NULL AUTO_INCREMENT PRIMARY KEY,
	`MEMBER_ID`	VARCHAR(100)	NOT NULL UNIQUE,
	`MEMBER_PASSWORD`	VARCHAR(100)	NOT NULL,
	`MEMBER_EMAIL`	VARCHAR(100)	NOT NULL UNIQUE,
	`MEMBER_NAME`	VARCHAR(100)	NOT NULL,
	`MEMBER_NICKNAME`	VARCHAR(100)	NOT NULL,
	`MEMBER_PHONE`	VARCHAR(100)	NOT NULL UNIQUE,
	`MEMBER_PROFILE_IMAGE_URL`	VARCHAR(100)	DEFAULT 'IMAGE/NOIMAGE.PNG',
	`MEMBER_DOB`	DATE	NOT NULL,
	`MEMBER_ENABLE` BOOLEAN,
	`REG_DT`	TIMESTAMP	NOT NULL
);

CREATE TABLE `NFT` (
	`NFT_HASH`	VARCHAR(100)	NOT NULL PRIMARY KEY,
	`NFT_OWNER_SEQ`	INT	NOT NULL,
	`NFT_CREATOR_SEQ`	INT	NOT NULL,
