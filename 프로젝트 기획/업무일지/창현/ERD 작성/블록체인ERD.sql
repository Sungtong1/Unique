	`TRANSACTION_FROM`	VARCHAR(100)	NOT NULL,
	`TRANSACTION_TO`	VARCHAR(100)	NOT NULL,
	`TRANSACTION_VALUE`	VARCHAR(100)	NOT NULL,
	`NFT_HASH`	VARCHAR(100)	NOT NULL,
	`REG_DT`	TIMESTAMP	NOT NULL
